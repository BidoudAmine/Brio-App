package com.company;

public class User {
    int id  ;

    String login ;
    String mdp ;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }



    public String getLogin() {
        return login;
    }

    public void setLogin(String login) {
        this.login = login;
    }

    public String getMdp() {
        return mdp;
    }

    public void setMdp(String mdp) {
        this.mdp = mdp;
    }
}
